# Write a function definition named half that takes in a number and returns half the provided number.

def half(number):
    return number / 2

assert half(4) == 2
assert half(5) == 2.5
assert half(3) == 3 / 2
assert half(2) == 2 / 2
assert half(-3) == -3 / 2
assert half(-4) == -4 / 2
print("Exercise 21 is correct.")