# Write a function definition named triple that takes in a number and returns triple the provided number.

def triple(number):
  return number * 3

assert triple(4) == 12
assert triple(5) == 15
assert triple(1) == 1 * 3
assert triple(2) == 2 * 3
assert triple(-3) == -3 * 3
assert triple(-4) == -4 * 3
print("Exercise 23 is correct.")