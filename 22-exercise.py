# Write a function definition named double that takes in a number and returns double the provided number.
def double(number):
  return number * 2
assert double(4) == 8
assert double(5) == 10
assert double(1) == 1 * 2
assert double(2) == 2 * 2
assert double(-3) == -3 * 2
assert double(-4) == -4 * 2
